package 변수;

public class Ex04문자열 {

	public static void main(String[] args) {
		
		//문자열 Hello world!
		//String : reference Type, "" -> 대문자로 시작
		//값 저장하는 것 외에 기능이 있다
		String s = "Hello world";
		
		//char : primitive Type(기본타입) -> 소문자로 시작
		//값 저장하는 것 외에 기능이 없다.
		//Char c = 'H';
		
		boolean d = true;
		System.out.println(d);
		
		char c = 'A';
		System.out.println(c);
		
		System.out.println(s);
		
		
	
	}

}
