package 메서드;

public class Ex04매x리o {

	public static void main(String[] args) {
		getName();
		System.out.println(getName());
		// "서유광"
	}
 
	
	// getName() 만들기 : 매개변수x 리턴타입o
	// () ---> ()
	public static String getName() {
		// 리턴타입에 자료형이 있으면 return이라는 키워드가 따라온다.!
		return "서유광";
	}
}
